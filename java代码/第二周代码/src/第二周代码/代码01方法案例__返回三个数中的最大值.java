package 第二周代码;

import java.util.Scanner;

public class 代码01方法案例__返回三个数中的最大值 {
public static int compare(int a,int b,int c)
{
	int max;
	if(a>b){
		max=a;
	}
	else {
		max=b;
	}
	if(c>max) {
		max=c;
	}
	return max;
}
public static void main(String[] args) {
	Scanner sc = new Scanner(System.in);
	System.out.println("请输入你分别输入要比较的三个数：");
	int a=sc.nextInt();
	int b=sc.nextInt();
	int c=sc.nextInt();
	int max;
	max=compare(a,b,c);
	System.out.println("三个数中最大数为"+max);
}
}
